public class tipoVar {
    public static void main(String[] args) {
        var nombre = "Carlos";
        System.out.println("nombre: " + nombre);
        var edad = 20;
        System.out.println("edad:" + edad);
        var sueldo = 5000.5F;
        var esCasado = true;
        System.out.println("sueldo: " + sueldo);
        System.out.println("es casado: " + esCasado);
    }
}
